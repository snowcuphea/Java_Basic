package chap04;
//for문 연습 - 기본 for연습(for문의 문법)
public class ForTest01 {
	public static void main(String[] args) {
		for (int i = 1; i<=5;i++) {
			System.out.println("1.java programming"+i);
		}
		System.out.println("************************");
		for (int i = 11; i<=15;i++) {
			System.out.println("2.java programming");
		}
		System.out.println("************************");
		for (int i = 3; i>=1;i--) {
			System.out.println("3.java programming");
		}
		System.out.println("************************");
		for (int i = 1; i<=5;i++) {
			System.out.println("4.java programming");
		}
		System.out.println("************************");
		for (int i = 1; i<=5;i++) {
			System.out.println("5.java programming");
		}
		System.out.println("************************");

	}

}
